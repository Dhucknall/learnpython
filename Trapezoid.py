#Calculate the area of a trapezoid
height = float(input("Please enter the height of the Trapezoid: "))
length_a = float(input("Please enter length of base of Trapezoid: "))
length_b = float(input("Please enter the length of the top of Trapezoid: "))
answer = 0.5 * (length_a + length_b) * height
print (" The area of the trapezoid is : ", answer)
